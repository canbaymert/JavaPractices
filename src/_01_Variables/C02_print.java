package _01_Variables;

public class C02_print {
    /*
	  \n (or \r): starts from a new line
	          \t:  horizontal tab
	          \\: prints a back slash
	          \': prints single quote
	          \": prints double quote
	 */
    public static void main(String[] args) {
        System.out.println("   \"Hello \" , \"World \\ / \"    ");
        //output: "Hello ", "World  \ /"

        System.out.println("m\no\nn\nd\na\ny");
        /* output:
            m
            o
            n
            d
            a
            y
         */
    }
}

